# Какой язык объяснений выбрать по уровню, если стоит "auto"
AUTO_LANGUAGE_BY_LEVEL = {
    "beginner": "ru",
    "intermediate": "ru",
    "advanced": "en",
    "unknown": "ru",
}


def build_pronounce_request(word: str) -> str:
    """Инструкция для инструмента «Как читается»."""
    return f"""Покажи, как читается: «{word}».

Формат ответа (строго, без markdown и emoji):
{word}

Британский: [русскими буквами] ([IPA])
Американский: [русскими буквами] ([IPA])

Если вариант один — одна строка:
Произношение: [русскими буквами] ([IPA])

Правила:
- Каждый вариант — отдельная строка, не сливай IPA и варианты в один абзац
- Сначала русскими буквами, IPA в скобках после
- Не предлагай голосовые сообщения, аудио и запись — бот их не принимает
- Только текст, кратко, без лишних комментариев"""


def build_meaning_request(word: str) -> str:
    """Инструкция для инструмента «Непонятно слово»."""
    return (
        f"Объясни простыми словами, что значит: «{word}».\n\n"
        f"Формат ответа (строго, только два блока с emoji, без markdown):\n\n"
        f"{MEANING_RESPONSE_FORMAT}\n\n"
        f"Правила:\n{MEANING_RESPONSE_RULES}"
    )


def build_system_prompt(
    style: str, level: str, explanation_language: str = "auto", topic: str = None
) -> str:
    """Собрать полный системный промпт под пользователя."""
    style_block = NEUTRAL_STYLE
    level_block = LEVEL_PROMPTS.get(level, LEVEL_PROMPTS["unknown"])

    if explanation_language == "auto":
        lang = AUTO_LANGUAGE_BY_LEVEL.get(level, "ru")
    else:
        lang = explanation_language
    lang_block = LANGUAGE_PROMPTS.get(lang, LANGUAGE_PROMPTS["ru"])
    format_block = RESPONSE_FORMAT_BY_LANG.get(lang, RESPONSE_FORMAT_BY_LANG["ru"])

    prompt = (
        f"{BASE_PROMPT}\n\n{WORD_MEANING_IN_DIALOG}\n\n{style_block}\n\n{level_block}\n\n"
        f"{lang_block}\n\n{format_block}"
    )

    # Если идет разговор по теме — добавляем инструкцию
    if topic:
        prompt += (
            f"\n\nТЕМА РАЗГОВОРА: сейчас вы общаетесь на тему «{topic}». "
            f"Веди беседу вокруг этой темы, задавай вопросы по ней. "
            f"НО если пользователь спросит о другом (грамматика, слово, перевод) — "
            f"сначала помоги с его вопросом, потом мягко вернись к теме «{topic}»."
        )
    return prompt
